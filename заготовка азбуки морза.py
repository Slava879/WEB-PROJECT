def combine_mp3(sp, output_file):
    with open(output_file, "wb") as output:
        for file in sp:
            with open(file, "rb") as text:
                output.write(text.read())


files = []
for i in input():
    if i.lower() in morse_code_dict:
        for j in morse_code_dict[i.lower()]:
            if j == '.':
                files.append('audio/morse_dot.mp3')
            elif j == '-':
                files.append('audio/morse_tire.mp3')
            elif j == '':
                files.append('audio/spase.mp3')
            else:
                print(j)

output_file = "audio/combined.mp3"
